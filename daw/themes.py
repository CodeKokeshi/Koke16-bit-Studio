# Each entry: (display_name, description, builder_func)
THEME_CATALOGUE: dict[str, tuple[str, str, object]] = {
    "retro_neon":  ("Retro Neon",   "Original Koke16 — neon cyan on black",  _retro_neon),
    "aero_glass":  ("Aero Glass",   "Windows Vista/7-inspired translucent",  _aero_glass),
    "win11":       ("Windows 11",   "Modern Fluent UI / Mica-like",          _win11),
    "macos":       ("macOS",        "Apple Aqua-inspired rounded look",      _macos),
    "dos":         ("DOS / CGA",    "Retro MS-DOS terminal style",           _dos),
    "solarized":   ("Solarized",    "Ethan Schoonover's precision palette",  _solarized),
    "nord":        ("Nord",         "Arctic north-bluish colour scheme",     _nord),
    "dracula":     ("Dracula",      "Popular dark purple dev theme",         _dracula),
    "gameboy":     ("Game Boy",     "Original DMG 4-shade green LCD",        _gameboy),
}

DEFAULT_THEME = "retro_neon"
DEFAULT_MODE = "dark"


def get_theme_tokens(theme_id: str, mode: str) -> ThemeTokens:
    """Build final ``ThemeTokens`` for the given *theme_id* and *mode*."""
    base = _dark_base() if mode == "dark" else _light_base()
    _name, _desc, builder = THEME_CATALOGUE.get(theme_id, THEME_CATALOGUE[DEFAULT_THEME])
    return builder(base)


def get_theme_qss(theme_id: str, mode: str) -> str:
    """One-call convenience — returns the full QSS string."""
    return build_qss(get_theme_tokens(theme_id, mode))
