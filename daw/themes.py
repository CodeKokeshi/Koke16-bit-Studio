"""Theme definitions for Koke16-Bit Studio.

Each theme is a dict of colour tokens consumed by the QSS template in
``build_qss()``.  A colour-mode (``"dark"`` or ``"light"``) selects base
brightness, then the *visual theme* overrides accent colours, radii,
borders, fonts, and extra QSS snippets.
"""
from __future__ import annotations

from dataclasses import dataclass, field


# ────────────────────────────────────────────────────────────────────
# Token dataclass that every theme provides
# ────────────────────────────────────────────────────────────────────

@dataclass
class ThemeTokens:
    # Colour mode the tokens were built for
    mode: str = "dark"                  # "dark" | "light"

    # Base surfaces
    bg_window: str = "#101010"
    bg_panel: str = "#111111"
    bg_input: str = "#121212"
    bg_hover: str = "#181818"
    bg_selected: str = "#1a2e2a"

    # Text
    fg_primary: str = "#e7e7e7"
    fg_secondary: str = "#aaaaaa"
    fg_muted: str = "#777777"
    fg_disabled: str = "#555555"

    # Accent
    accent: str = "#00ffc8"
    accent_hover: str = "#00ffc8"
    accent_bg: str = "#1a2e2a"

    # Borders
    border: str = "#343434"
    border_panel: str = "#292929"
    border_input: str = "#2a2a2a"
    border_separator: str = "#2e2e2e"
    border_item: str = "#1a1a1a"

    # Buttons
    btn_bg: str = "#1f1f1f"

    # Danger / active
    danger: str = "#ff4444"
    mute_active: str = "#e74c3c"
    solo_active: str = "#f1c40f"

    # Rounding
    radius: str = "6px"
    radius_panel: str = "8px"

    # Title
    title_size: str = "20px"

    # Font family (empty = inherit theme default)
    font_family: str = "Segoe UI"

    # Extra QSS appended verbatim after the generated base sheet
    extra_qss: str = ""


# ────────────────────────────────────────────────────────────────────
# QSS template builder
# ────────────────────────────────────────────────────────────────────

